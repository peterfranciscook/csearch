%
%   argmax = csearch(x, 'lt', y) is equivalent to MATLAB's find(x<y, 1, 'last')
%
%   argmax = csearch(x, 'le', y) is equivalent to MATLAB's find(x<=y, 1, 'last')
%
%   argmin = csearch(x, 'gt', y) is equivalent to MATLAB's find(x>y, 1, 'first')
